    public void escrever_arquivo() throws IOException {
        try {
            FileOutputStream writeData = new FileOutputStream("bd.txt");

            ObjectOutputStream writeStream = new ObjectOutputStream(writeData);

            writeStream.writeObject(db);
            writeStream.flush();
            writeStream.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

